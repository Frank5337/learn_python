print(9 // 4)  # 2
print(-9 // -4)  # 2
print(9 // -4)  # -3
print(-9 // 4)  # -3  一正一负的正数公式, 向下取数

print(9 % -4)  # -3 公式 余数 = 被除数-除数*商 9-(-4)*(-3)
print(-9 % 4)  # 3            -9-4*(-3)
print(-11 % 4)  # 1            -11-4*(-3)
print(15 % -6)  # -3            15-(-6)*(-3)
